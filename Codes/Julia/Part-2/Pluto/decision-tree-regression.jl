# ╔═╡ 0e8185f5-8ace-4407-a89e-487de2272b26
md"Load data from CSV file"

# ╔═╡ b04b74cc-3d21-4f15-af05-e938c4ab96dc
df = CSV.read("../../Datasets/50_Startups.csv", DataFrame)

# ╔═╡ 1e9b340a-3361-4910-a274-213948835e51
schema(df)

# ╔═╡ 2fbfb07a-4c79-4ae2-895d-e0aac8982bfa
md"Design the features"

# ╔═╡ 76ff10b3-ac7c-4a84-8817-63dfbc942379
Data = df[!, 1:4]

# ╔═╡ ebb147a6-574d-4872-b3f8-ff591ff4db6f
colnames = ["rd", "admin", "spend", "state"]

# ╔═╡ 6908b62b-1862-4095-94b5-1da02f4a754a
begin
	rename!(Data, Symbol.(colnames))
	coerce!(Data, :state => Multiclass)
end

# ╔═╡ 0eebfbb6-eff2-4bfd-801e-7202b8f1dc82
md"Encoding the state column"

# ╔═╡ 2e4a082e-1361-458e-983b-a4cddb1e9af8
ce = ContinuousEncoder()

# ╔═╡ 5a76349e-5f54-44bc-96bf-ddcb03e7277a
X = machine(ce, Data) |> fit! |> MLJ.transform

# ╔═╡ 8605bf99-e650-4af0-a950-4330cc94cb34
md"Extract target vector"

# ╔═╡ 1caa8438-8b53-4035-aa0a-348ff6879cf8
y = df.Profit

# ╔═╡ 0a45e3dd-f18f-48bd-862b-5278ff97ebcd
md"Preparing for the split"

# ╔═╡ a173053b-b562-456f-9d9c-b917c0a602ea
train, test = partition(eachindex(y), 0.8, shuffle=true, rng=123)

# ╔═╡ 9e7aae35-55ec-4250-ace8-36e149bfba19
Xtrain, Xtest = X[train, :], X[test, :]

# ╔═╡ 98a4ad83-bf2c-440c-a90e-562a145ae098
ytrain, ytest = y[train], y[test]

# ╔═╡ 710e7a2f-640e-4a31-bc81-7d9b5af79037
md"Load & instantiate the decision tree regression model"

# ╔═╡ 6e80aaa3-2e97-435a-afc3-e120af315b38
DTR = @load DecisionTreeRegressor pkg=DecisionTree

# ╔═╡ b33a1df8-a49a-4de5-a8b9-1bd0f7e98361
dtr_ = DTR(max_depth=5, min_samples_split=3)

# ╔═╡ 0c16897b-3293-4c41-8aef-1e5c66ad854f
md"You may want to see [DecisionTree.jl](https://github.com/bensadeghi/DecisionTree.jl) and the unwrapped model type [`MLJDecisionTreeInterface.DecisionTree.DecisionTreeRegressor`](@ref)."

# ╔═╡ 137b93ce-9a2c-48d3-b131-f3dcdf3f5731
md"Train & fit"

# ╔═╡ 0251ba7d-a1ba-444a-9580-992b4bb2a377
dtr = machine(dtr_, Xtrain, ytrain) |> fit!

# ╔═╡ bb61370f-092a-40b6-9298-f7702f5d1e4d
println("Params of fitted model are $(fitted_params(dtr).tree)")

# ╔═╡ cc996e2e-852b-49ac-97fc-23d06fc09cb2
md"Prediction"

# ╔═╡ 1f98be8f-bf23-4d4d-a2be-01e1f23ca73d
